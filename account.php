            $('.img-delete').hide();
            $('input[type=file]').change(function(){
               // alert("dd");
                var ext = $('#image_profile').val().split('.').pop().toLowerCase();
                //Allowed file types
                if($.inArray(ext, ['png','jpg']) == -1) {
                    $('#alert_cancle2').modal('show');
                    $('#image_profile').val("");
                    //$('#upload-file-info').html("");
                }
             
                //Tested in Firefox and Google Chorme
                sizee = $("#image_profile")[0].files[0].size; //file size in bytes
                sizee = sizee / 1024; //file size in Kb
                sizee = sizee / 1024; //file size in Mb
                 
                //file size more than 10Mb
                if (sizee > 1) {
                    $('#alert_cancle').modal('show');
                    return false;
                }  else {
                    
                    readURL(this);//Ajax code to upload file to server
                }
                });

                function readURL(input) {

                  if (input.files && input.files[0]) {
                    var reader = new FileReader();
                     $('.img-delete').show();
                    reader.onload = function(e) {
                      $('.point-body .rounded').attr('src', e.target.result);
                    }

                    reader.readAsDataURL(input.files[0]);
                  }
                }
                
                $( ".img-delete" ).click(function() {
                    $('.point-body .rounded').attr('src','images/icon/icon-avatar-lg.png');
                     $('.img-delete').hide();
                });

