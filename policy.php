                                        <h4 class="color2">ข้อ 3 คำรับรองและค่ารับประกัน</h4>
                                        <p class="mb-3">คำนิยามของคำในเงื่อนไขการใช้คะแนนให้เป็นไปดังต่อไปนี้</p>

                                        <p>3.1 สมาชิกจะได้รับแจ้งข้อตกลงและเงื่อนไขการใช้บริการและคะแนนจากลูกค้า</p>
                                        
                                        <p>3.2 สมาชิกรับทราบว่าคุณสมบัติในการเป็นสมาชิก/ผู้ใช้บริการจะสิ้นสุดลงเมื่อสมาชิกได้เป็นพนักงานของลูกค้าหรือเมื่อลูกค้าได้ทำกำรลบการลงทะเบียนของสมาชิกออกจากระบบหรือบริษัทฯ ทำการยกเลิกคะแนนเป็นการถาวร สมาชิกจะต้องไม่ใช้คะแนนที่ได้รับหลังจากที่คุณสมบัติในการเป็นสมาชิกได้สิ้นสุดลง และต้องรับผิดในความเสียหายใดๆ ที่เกิดกับบริษัทฯ อันเนื่องมำจำกกำรฝ่ำฝืนเงื่อนไขข้อนี้้</p>
                                        
                                        <p>3.3 สมาชิกยอมรับว่าเป็นเรื่องปกติที่จะมีการขัดข้องหรือความผิดพลาดในการเข้าสู่ระบบซึ่งสมาชิกจะไม่เรียกร้องค่าใช้จ่าย หรือค่าเสียหายใดๆ จากบริษัทฯ</p>
                                        
                                        <p>3.4 สมาชิกอนุญาตให้บริษัทใช้ข้อมูลที่ลูกค้าหรือสมาชิก/ผู้ใช้บริการได้ให้แก่บริษัทฯ เพื่อทำการตรวจสอบ
                                        วิเครำะห์ ทำกำรสำรวจ และกระทำการอื่นๆ และอนุญาตให้บริษัทฯ เปิดเผยข้อมูลดังกล่าวแก่ผู้รับจ้างช่วงของบริษัทฯ</p>
                                        
