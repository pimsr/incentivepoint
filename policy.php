                                        <p>(3) “คะแนนที่ได้รับ” หมายถึง คะแนนที่ใช้ได้ซึ่งสมาชิกได้รับจากลูกค้าและเป็นคะแนนที่ลงบันทึกในระบบเพื่อใช้แลกเปลี่ยนของรางวัลที่บริษัทฯ เสนอให้</p>
                                        <p>(4) “สมาชิก” หมายถึงบุคคลผู้ได้รับคะแนนจากลูกค้าและหลังจากทำการลงทะเบียนเรียบร้อยแล้ว ให้ถือว่าสมาชิกเป็นผู้ใช้บริการภายใต้ระบบ</p>
                                        <p>(5) “คะแนน” หมายถึง คะแนนจูงใจของบริษัทฯที่ลูกค้าได้ซื้อและได้มอบให้แก่สมาชิกหรือผู้ใช้บริการเพื่อทำการลงทะเบียนเข้าสู่ระบบของบริษัทฯ</p>
                                        <p>(6) “ของรางวัล” หมายถึง สินค้า และ/หรือ บริการที่บริษัทฯเสนอให้สมาชิกหรือผู้ใช้บริการเพื่อแลกกับคะแนนที่ได้รับจากลูกค้า</p>
