                                        <h4 class="color2">ข้อ 2 การใช้บริการและคะแนนที่ได้รับ</h4>
                                        <p class="mb-3">คำนิยำมของคำในเงื่อนไการใช้คะแนนให้เป็นไปดังต่อไปนี้</p>

                                        <p>2.1 สมาชิกจะต้องใช้บริการและคะแนนที่ได้รับโดยกรอกข้อมูลตามที่บริษัทฯ กำหนด เช่น ชื่อ ที่อยู่ เบอร์โทรศัพท์ วันเดือนปีเกิด และอีเมล์</p>
                                        <p>2.2 เมื่อทำการลงทะเบียนเขำสู่ระบบเสร็จสมบูรณ์ ให้ถือว่าสมาชิกได้เข้าใจและยอมรับเงื่อนไขและกฎเกณฑ์การใช้บริการแล้ว</p>
                                        <p>2.3 สมาชิกสำมารถใช้บริการตามวิธีการหรือขั้นตอนการแลกคะแนนเพื่อรับของรางวัลบนเว็บไชต์ของบริษัทฯ ตั้งแต่เวลา 05.00 น. ถึง 02.00 น. ได้ทุกวันรวมถึงวันหยุดนักขัตฤกษ์ต่างๆ อย่างไรก็ตามบริษัทฯ จะให้คำแนะนำหรือข้อมูลเกี่ยวกับการให้บริการหรือระบบในเวลา 
                                        10.00 น. ถึง 18.00 น. ทุกวันจันทร์ถึงวันศุกร์เท่านั้นเว้นแต่วันดังกล่าวจะเป็นวันหยุดของบริษัทฯ อย่างไรก็ตามหากเวลาให้บริการดังกล่าวมีการเปลี่ยนแปลง บริษัทฯ จะแจ้งให้ทราบล่วงหน้า</p>
                                        <p>2.4 สมาชิกจะต้องใช้คะแนนที่ได้รับเพื่อแลกรับของรางวัลภายในระยะเวลาที่กำหนดในระบบหรือระยะเวลาของสัญญาให้บริการ นอกจากนี้ การใช้คะแนนที่ได้รับอาจถูกยกเลิกเป็นการชั่วคราวหรือถาวรโดยมิต้องแจ้งให้ทราบล่วงหน้าหากมีการผิดสัญญาให้บริการหรือเงื่อนไขการใช้คะแนน หรือในกรณีที่สัญญาให้บริการสิ้นสุดลง หรือสมาชิกไม่มีคุณสมบัติที่จะใช้คะแนนได้ ในกรณีนี้ สมาชิกไม่มีสิทธิเรียกร้องค่าเสียหายจากบริษัทฯ</p>
                                        <p>2.5 สมาชิกจะต้องใช้คะแนนที่ได้รับเพื่อแลกของรางวัลเท่านั้น สมาชิกไม่สามารถขอคืนเงินและแลกคะแนนเพื่อรับเงินสดได้ ในกรณีที่ของรางวัลที่ได้แลกคะแนนมีกำหนดระยะเวลาการใช้และสมาชิก/ผู้ใช้บริการไม่ได้ใช้ของรางวัลดังกล่าวภายในระยะเวลาที่กำหนดไม่ว่าด้วยเหตุใดก็ตาม ให้ถือว่าสมาชิก/ผู้ใช้บริการได้ใช้ของรางวัลดังกล่าวแล้ว</p>
                                        <p>2.6 สมาชิกไม่สามารถโอนคะแนนที่ได้รับให้แก่บุคคลภายนอกหรือคะแนนไม่สามารถถูกโอนด้วยการรับมรดกได้ อย่างไรก็ตามคะแนนนั้นอาจถูกโอนไปยังบุคคลภายนอกได้ในกรณีของการส่งเสริมการขายพิเศษของบริษัทฯ เท่านั้น 
                                        <p> 2.7 ของรางวัลที่ได้ใช้คะแนนแลกนั้นจะถูกจัดส่งให้แก่ผู้ใช้บริการไปยังที่อยู่ของลูกค้าตามที่ระบุในระบบหรือจัดส่งโดยตรงไปยังสมาชิกผ่านอีเมลล์ที่ระบุในระบบ ให้ถือว่าสมาชิกได้รับของรางวัลเมื่อของรางวัลถูกส่งไปยังที่อยู่หรืออีเมลล์ดังกล่าว</p>
                                        <p>2.8 ของรางวัลที่ได้แลกหรือได้จัดส่งไปยังที่อยู่ของลูกค้าไม่สามารถยกเลิกหรือส่งคืนได้ในทุกกรณี อย่างไรก็ตาม ในกรณีที่มีความชำรุดบกพร่องของสินค้าซึ่งไม่ใช่ความชำรุดบกพร่องอันเกิดจากความผิดของสมาชิก/ผู้ใช้บริการ สมาชิก/ผู้ใช้บริการจะต้องแจ้งให้ผู้ผลิตสินค้าหรือบริษัทฯ ทราบเป็นลายลักษณ์อักษรภายใน 2 เดือนนับจากวันที่ส่งมอบสินค้า สมาชิก/ผู้ใช้บริการสามารถเปลี่ยนสินค้าที่ชำรุดบกพร่องนั้นเป็นสินค้าชิ้นใหม่ได้</p>
                                        <p>2.9 บริษัทฯ มีสิทธิในกำรเปลี่ยนจ ำนวนสินค้ำหรือรำยกำรของรำงวัลตำมที่บริษัทฯ เห็นสมควรโดยไม่ต้องแจ้งให้สมำชิก/ผู้ใช้บริกำร
                                        ทราบล่วงหน้า</p>
